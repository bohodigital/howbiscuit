import { existsSync } from 'node:fs';
import { mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { compileLatexArticle, generatedMdx, generatedModule } from '../src/lib/latex/article-compiler.mjs';

const root = process.cwd();
const sourceRoot = path.join(root, 'content', 'latex', 'articles');
const articleRoot = path.join(root, 'src', 'content', 'docs', 'articles');
const moduleRoot = path.join(root, 'src', 'generated', 'latex');
const checkOnly = process.argv.includes('--check');
const marker = 'Generated by compile-latex-articles.mjs';

async function collectTex(directory) {
  if (!existsSync(directory)) return [];
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) files.push(...(await collectTex(fullPath)));
    else if (entry.isFile() && entry.name.endsWith('.tex')) files.push(fullPath);
  }
  return files.sort();
}

async function generatedFlatMdx() {
  if (!existsSync(articleRoot)) return [];
  const entries = await readdir(articleRoot, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith('.mdx')) continue;
    const fullPath = path.join(articleRoot, entry.name);
    const source = await readFile(fullPath, 'utf8');
    if (source.includes(marker)) files.push(fullPath);
  }
  return files;
}

async function generatedModules(directory = moduleRoot) {
  if (!existsSync(directory)) return [];
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) files.push(...(await generatedModules(fullPath)));
    else if (entry.isFile() && entry.name.endsWith('.mjs')) files.push(fullPath);
  }
  return files.sort();
}

async function assertExact(filePath, expected, errors) {
  if (!existsSync(filePath)) {
    errors.push(`Missing generated file: ${path.relative(root, filePath)}`);
    return;
  }
  const actual = await readFile(filePath, 'utf8');
  if (actual !== expected) errors.push(`Stale generated file: ${path.relative(root, filePath)}`);
}

const sources = await collectTex(sourceRoot);
if (!sources.length) throw new Error('No LaTeX article sources found in content/latex/articles.');

const outputs = [];
const slugs = new Set();
for (const filePath of sources) {
  const sourcePath = path.relative(root, filePath).replaceAll(path.sep, '/');
  const article = compileLatexArticle(await readFile(filePath, 'utf8'), { sourcePath });
  if (slugs.has(article.metadata.slug)) throw new Error(`Duplicate LaTeX article slug: ${article.metadata.slug}`);
  slugs.add(article.metadata.slug);
  outputs.push({
    article,
    mdxPath: path.join(articleRoot, `${article.metadata.slug}.mdx`),
    mdx: generatedMdx(article),
    modulePath: path.join(moduleRoot, `${article.metadata.slug}.mjs`),
    module: generatedModule(article),
  });
}

if (checkOnly) {
  const errors = [];
  for (const output of outputs) {
    await assertExact(output.mdxPath, output.mdx, errors);
    await assertExact(output.modulePath, output.module, errors);
  }
  const expectedMdx = new Set(outputs.map((output) => path.resolve(output.mdxPath).toLowerCase()));
  for (const filePath of await generatedFlatMdx()) {
    if (!expectedMdx.has(path.resolve(filePath).toLowerCase())) {
      errors.push(`Orphaned generated file: ${path.relative(root, filePath)}`);
    }
  }
  const expectedModules = new Set(outputs.map((output) => path.resolve(output.modulePath).toLowerCase()));
  for (const filePath of await generatedModules()) {
    if (!expectedModules.has(path.resolve(filePath).toLowerCase())) {
      errors.push(`Orphaned generated module: ${path.relative(root, filePath)}`);
    }
  }
  if (errors.length) throw new Error(errors.join('\n'));
  console.log(`LaTeX article check passed for ${outputs.length} document(s).`);
} else {
  await mkdir(articleRoot, { recursive: true });
  for (const filePath of await generatedFlatMdx()) await rm(filePath);
  await rm(moduleRoot, { recursive: true, force: true });
  await mkdir(moduleRoot, { recursive: true });
  for (const output of outputs) {
    await writeFile(output.mdxPath, output.mdx, 'utf8');
    await writeFile(output.modulePath, output.module, 'utf8');
  }
  console.log(`Compiled ${outputs.length} LaTeX article(s) into static Astro content.`);
}
